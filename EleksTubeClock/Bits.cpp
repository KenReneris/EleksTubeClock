int
FirstSetBit8( uint8 bits )
{
    return k_firstSetBit[ bits ];
}


int
FirstSetBit16( uint16 bits )
{
    if ( bits & 0xFF )
    {
        return k_firstSetBit[ bits ];
    }
    else
    {
        return 8 + k_firstSetBit[ bits >> 8 ];
    }
}
